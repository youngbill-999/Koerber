CREATE TABLE gdl_graph
(
  id_graph           VARCHAR(15) not null,
  typ_graph          VARCHAR(10),
  graph_descr        VARCHAR(25) not null,
  component          VARCHAR(22),
  creation_component VARCHAR(30),
  update_component   VARCHAR(30),
